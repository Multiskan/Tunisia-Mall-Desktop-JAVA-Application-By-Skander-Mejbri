    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ResponsableDao cd = new ResponsableDao();
        if (jTextField1.getText().trim().length() == 0 || jTextField2.getText().trim().length() == 0 || jTextField3.getText().trim().length() == 0 || jTextField5.getText().trim().length() == 0 || jPasswordField1.getText().trim().length() == 0 || jPasswordField2.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Vous devez remplir tout les champs.");
        } else if ((!jPasswordField1.getText().equals(jPasswordField2.getText()))) {
            JOptionPane.showMessageDialog(this, "Les mots de passes ne sont pas identiques. ");
        }else if ( cd.verifUser2(jTextField5.getText())== true ){
            JOptionPane.showMessageDialog(this, "ce username existe dejà. ");
        }
        else {
            Responsable c = new Responsable(jTextField1.getText(), jTextField2.getText(), jTextField3.getText(), jTextField5.getText(), jPasswordField1.getText());
            try {
                cd.add2(c);
            } catch (NoSuchAlgorithmException ex) {
                Logger.getLogger(GestionResponsable.class.getName()).log(Level.SEVERE, null, ex);
            }
            jTable1.setModel(new ListeResponsable());
            jTable2.setModel(new ListeResponsable());
            jTable3.setModel(new ListeResponsable());
            JOptionPane.showMessageDialog(null, "responsable " + jTextField1.getText() + jTextField2.getText() + " est ajouté");
        }
       
    }//GEN-LAST:event_jButton1ActionPerformed

