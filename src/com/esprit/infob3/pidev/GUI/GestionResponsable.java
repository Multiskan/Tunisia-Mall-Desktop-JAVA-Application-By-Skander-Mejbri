    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int row = jTable2.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(null, "Vous devez Selectionner le responsable a activer d'abord.");
        } else {if ((! jRadioButton1.isSelected() && ! jRadioButton2.isSelected()) || ( jRadioButton1.isSelected() &&  jRadioButton2.isSelected())  ){
            JOptionPane.showMessageDialog(null, "Vous devez selectionner une seule option.");
        }
        else{
            ResponsableDao cd = new ResponsableDao();
            Responsable c;
            c = cd.findById2((int) jTable2.getValueAt(jTable2.getSelectedRow(), 0));

            if (jRadioButton1.isSelected()) {
                if (c.getEtat() == 1) {
                    JOptionPane.showMessageDialog(null, "Ce compte  est déjà activé.");
                } else {

                    c.setEtat(1);
                    cd.updateEtat2(c);
                    jTable1.setModel(new ListeResponsable());
                jTable2.setModel(new ListeResponsable());
                jTable3.setModel(new ListeResponsable());
                    JOptionPane.showMessageDialog(null, "Ce compte est activé.");
                    
                }
            } else if (jRadioButton2.isSelected()) {
                if (c.getEtat() == 0) {
                    JOptionPane.showMessageDialog(null, "Ce compte de"+ c.getNom()+" est déjà désactivé.");
                } else {

                    c.setEtat(0);
                    cd.updateEtat2(c);
                    jTable1.setModel(new ListeResponsable());
                jTable2.setModel(new ListeResponsable());
                jTable3.setModel(new ListeResponsable());
                    JOptionPane.showMessageDialog(null, "Ce compte de "+ c.getNom()+ " est désactivé.");
                    
                    
                }
            }
        }
        }
                
    }//GEN-LAST:event_jButton5ActionPerformed

